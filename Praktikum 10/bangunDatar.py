import math

def jajarGanjar(alas, tinggi):
    return alas * tinggi

def persegi(sisi):
    hasil = sisi * sisi
    return hasil

def persegi_panjang(panjang, lebar):
    hasil = panjang * lebar
    return hasil

def segitiga (alas, tinggi):
    hasil = 0.5 * alas * tinggi
    return hasil

def lingkaran(jari_jari):
    hasil = math.pi * jari_jari * jari_jari
    return hasil